#include <stdio.h>  // เรียกใช้ไลบรารีสำหรับรับ-แสดงผลทางหน้าจอ

int main() {
    // ประกาศตัวแปร:
    // name[3][10]  -> เก็บชื่อ 3 คน คนละไม่เกิน 9 ตัวอักษร (+1 สำหรับ '\0')
    // grade[3]     -> เก็บเกรดของแต่ละคน
    // age[3]       -> เก็บอายุของแต่ละคน
    // height[3]    -> เก็บส่วนสูงของแต่ละคน
    // weight[3]    -> เก็บน้ำหนักของแต่ละคน
    char name[3][10], grade[3];
    int age[3];
    float height[3], weight[3];

    // วนลูปเพื่อรับข้อมูล 3 คน
    for (int i = 0; i < 3; i++) {
        // แสดงข้อความให้ผู้ใช้กรอกข้อมูลในรูปแบบที่กำหนด
        printf("Enter data (Name*Age*Height*Weight*Grade): ");
        
        // รับข้อมูลจากผู้ใช้โดยใช้ * เป็นตัวคั่น
        // %[^*] -> อ่าน string จนเจอ * (ชื่อ)
        // %d   -> อ่านจำนวนเต็ม (อายุ)
        // %f   -> อ่านทศนิยม (ส่วนสูง, น้ำหนัก)
        // %c   -> อ่านตัวอักษร (เกรด)
        scanf(" %[^*]*%d*%f*%f*%c", name[i], &age[i], &height[i], &weight[i], &grade[i]);
    }

    // แสดงหัวตาราง
    printf("+------+-----+-----------+-----------+------------+\n");
    printf("| Name | Age | Height(cm)| Weight(kg)| Grade Code |\n");
    printf("+------+-----+-----------+-----------+------------+\n");

    // แสดงข้อมูลที่เก็บไว้ในรูปแบบตาราง
    for (int i = 0; i < 3; i++) {
        // ใช้ printf พร้อม format เพื่อจัดให้ข้อมูลตรงแนวสวยงาม:
        // %-4s   -> ชื่อ ชิดซ้าย กว้าง 4 ช่อง
        // %3d    -> อายุ ชิดขวา 3 ช่อง
        // %9.1f  -> ส่วนสูงและน้ำหนัก แสดงทศนิยม 1 ตำแหน่ง กว้าง 9 ช่อง
        // %-2c   -> เกรดชิดซ้าย กว้าง 2 ช่อง อยู่กลางกรอบ
        printf("| %-4s | %3d | %9.1f | %9.1f |     %-2c     |\n",
               name[i], age[i], height[i], weight[i], grade[i]);
    }

    // แสดงเส้นปิดท้ายตาราง
    printf("+------+-----+-----------+-----------+------------+\n");

    return 0;  // สิ้นสุดโปรแกรม
}
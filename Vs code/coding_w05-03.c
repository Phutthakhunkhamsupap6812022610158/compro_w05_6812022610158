#include <stdio.h>  // ใช้สำหรับ printf()

int main() {
    // บรรทัดที่ 1:
    // แสดงข้อความ a ตามด้วย = และค่าต่าง ๆ ที่อยู่ห่างกันด้วย space ให้ตรงตามตาราง
    // มีการใส่ %% เพื่อแสดงเครื่องหมาย %
    printf("บรรทัดที่ 1    a =  0 . 6 1 7 %%\n");

    // บรรทัดที่ 2:
    // ช่องว่างระหว่าง b กับ = เยอะกว่าบรรทัดแรกเล็กน้อย และมีแค่เลข 2 กับ 5
    printf("บรรทัดที่ 2    b =    2 5 %%\n");

    // บรรทัดที่ 3:
    // ไม่มีตัวแปร a หรือ b เริ่มต้นด้วยช่องว่าง และตามด้วย = และตัวเลข 1 9 0 0 และ %
    printf("บรรทัดที่ 3    c =  1 9 . 0 0 0 %%\n");

    // บรรทัดที่ 4:
    // มีแค่ = กับค่าเดียวคือ 3 จัดไว้ด้านขวา
    printf("บรรทัดที่ 4    d    = 9  %%\n");

    // บรรทัดที่ 5:
    // = ขยับไปทางขวาอีก และค่าคือ 5 5 พร้อม %
    printf("บรรทัดที่ 5    e = 1 2 4 . 5 5 5 %%\n");

    // บรรทัดที่ 6:
    // เหมือนบรรทัดที่ 5 แต่ค่าคือ 0 1 ไม่มี %
    printf("บรรทัดที่ 6    f = 0 . 0 0 1 %%\n");

    return 0;  // คืนค่าจบโปรแกรม
}